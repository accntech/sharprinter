// ReSharper disable once CheckNamespace
namespace Sharprinter;

/// <summary>
///     Defines the vertical alignment options for table content.
/// </summary>
public enum VerticalAlignment
{
    /// <summary>
    ///     Aligns text to the top edge.
    /// </summary>
    Top = 0,

    /// <summary>
    ///     Centers text vertically.
    /// </summary>
    Center = 1,

    /// <summary>
    ///     Aligns text to the bottom edge.
    /// </summary>
    Bottom = 2
}