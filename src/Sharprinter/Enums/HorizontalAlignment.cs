namespace Sharprinter;

/// <summary>
///     Defines the alignment options for text and table content.
/// </summary>
public enum HorizontalAlignment
{
    /// <summary>
    ///     Aligns text to the left edge.
    /// </summary>
    Left = 0,

    /// <summary>
    ///     Centers text horizontally.
    /// </summary>
    Center = 1,

    /// <summary>
    ///     Aligns text to the right edge.
    /// </summary>
    Right = 2
}